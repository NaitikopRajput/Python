Number1=int(input("Enter Number 1 =>"))
Number2=int(input("Enter Number 2 =>"))

symbol=input("What do you want to do with this numbers ? ")

if symbol=="+":
    print("Answer =",Number1+Number2)
elif symbol=="-":
     print("Answer =",Number1-Number2)
elif symbol=="*":
     print("Answer =",Number1*Number2)
elif symbol=="/":
     print("Answer =",Number1/Number2)


