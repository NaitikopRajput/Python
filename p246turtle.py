import turtle
my_turtle = turtle.Turtle()
my_turtle.penup()
my_turtle.goto(-100, -100)
my_turtle.pendown()
my_turtle.goto(100, -100)
my_turtle.goto(100, 100)
my_turtle.goto(-100, 100)
my_turtle.goto(-100, -100)
my_turtle.penup()
my_turtle.goto(-100, 100)
my_turtle.pendown()
my_turtle.goto(0, 200)
my_turtle.goto(100, 100)
my_turtle.penup()
my_turtle.goto(-30, -100)
my_turtle.pendown()
my_turtle.goto(-30, -40)
my_turtle.goto(30, -40)
my_turtle.goto(30, -100)
my_turtle.penup()
my_turtle.goto(40, 0)
my_turtle.pendown()
my_turtle.goto(80, 0)
my_turtle.goto(80, 40)
my_turtle.goto(40, 40)
my_turtle.goto(40, 0)
turtle.done()