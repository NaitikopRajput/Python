import turtle
turtle.fillcolor('orange')
turtle.begin_fill()
turtle.goto(0,100)
turtle.goto(-100,0)
turtle.goto(0,0)
turtle.end_fill()
turtle.fillcolor('pink')
turtle.begin_fill()
turtle.goto(100,0)
turtle.goto(0,100)
turtle.goto(0,-100)
turtle.end_fill()
turtle.fillcolor('black')
turtle.begin_fill()
turtle.goto(-100,0)
turtle.goto(0,-100)
turtle.goto(100,0)

turtle.fillcolor('green')
turtle.begin_fill()
turtle.goto(0,-100)
turtle.goto(0,-150)
turtle.goto(0,-100)
turtle.end_fill()
turtle.fillcolor('yellow')
turtle.begin_fill()
turtle.goto(50,-150)
turtle.goto(-50,-150)
turtle.goto(0,-100)
turtle.end_fill()
turtle.done()